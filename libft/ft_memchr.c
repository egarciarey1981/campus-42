#include "libft.h"

void	*ft_memchr(const void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *) b;
	while (i < len && *ptr != (unsigned char) c)
	{
		i++;
		ptr++;
	}
	if (i == len)
		return (NULL);
	else
		return (ptr);
}
